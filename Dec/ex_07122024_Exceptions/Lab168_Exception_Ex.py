# Exception in Python are events that occur during the execution of a program that disrupt the
# normal flow of instructions
# A way to handle errors and unexpected situations gracefully
# Types of Exceptions
# Python has many built in exceptions that are raised when specific errors occur. Some common exceptions include,
# NameError: Raised when a local or global name is not found
# TypeError: Raised when an operation is performed on an incompatible data type
# ValueError: Raised when a function receives an argument of the correct type but an inappropriate value
# IndexError: Raised when trying to access an index that doesn't exist in a sequence
# KeyError: Raised when a key is not found in a dictionary
# ZeroDivisionError: Raised when dividing by zero

